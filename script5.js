// Реализовать основные 4 арифметические операции в виде функций с двумя параметрами.
// Обязательно использовать оператор return.

let a, b;

function sum(a,b)
{
    return a+b;
}
function razn(a,b)
{
    return a-b;
}
function mul(a,b)
{
    return a*b;
}
function del(a,b)
{
    return a/b;
}

alert(sum(3,5));
alert(razn(8,5));
alert(mul(3,5));
alert(del(15,5));